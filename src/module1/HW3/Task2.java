public class Task2 {
    public static void main(String[] args) {
        int num = 10;
        while (num <= 20) {
            System.out.print(num + "     ");
            num++;
        }
        num = 10;
        System.out.println();
        while (num <= 20) {
            System.out.print(num * num + "    ");
            num++;
        }
    }
}
